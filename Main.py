from cProfile import label
import kivy
import kivy.uix.button as button
from kivy.app import App
from kivy.uix.label import Label
from kivy.uix.gridlayout import GridLayout
from kivy.uix.textinput import TextInput
from kivy.uix.button import Button


class MyGridLayout(GridLayout):
    def __init__(self, **kwargs):
        super(MyGridLayout, self).__init__(**kwargs)
        # add columns
        self.cols = 2
        # add widgets
        self.add_widget(Label(text="Name: "))
        # add input box
        self.name = TextInput(multiline=False)
        self.add_widget(self.name)

        self.add_widget(Label(text="Age: "))
        # add input box
        self.age = TextInput(multiline=False)
        self.add_widget(self.age)

        self.add_widget(Label(text="Gender: "))
        # add input box
        self.gender = TextInput(multiline=False)
        self.add_widget(self.gender)
        # add a button
        self.btn1 = Button(text='Hello world 1')
        self.btn1.bind(on_press=self.callback)
        self.add_widget(self.btn1)
        # add a button
        self.btn2 = Button(text='Hello world 2')
        self.btn2.bind(on_press=self.callback)
        self.add_widget(self.btn2)
        # add a button
        self.btn3 = Button(text='Hello world 3')
        self.btn3.bind(on_press=self.callback)
        self.add_widget(self.btn3)
        # add a button
        self.btn4 = Button(text='Hello world 4')
        self.btn4.bind(on_press=self.callback)
        self.add_widget(self.btn4)

        # Create a submit button
        self.submit = Button(text="Submit", font_size=40)
        # bind the button
        self.submit.bind(on_press=self.press)
        self.add_widget(self.submit)

    # print in terminal what button was pressed

    def callback(self, instance):
        print('The button <%s> is being pressed' % instance.text)

    def press(self, instance):
        name = self.name.text
        age = self.age.text
        gender = self.gender.text
        # print output inside the program!
        self.add_widget(Label(
            text=f"Hello {name} You are already {age} years old and you are a {gender}! and you are a Cunt!"))
        # print the output in terminal
        print(
            f"Hello {name} You are already {age} years old and you are a {gender}!")

        # clear the input boxes
        self.name.text = ""
        self.age.text = ""
        self.gender.text = ""


class MyApp(App):
    def build(self):
        return MyGridLayout()


if __name__ == '__main__':
    MyApp().run()



